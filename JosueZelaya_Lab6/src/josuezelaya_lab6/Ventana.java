  public void ActualizarArbol() {
        //arbol
        DefaultTreeModel arbol = (DefaultTreeModel) arbol_genealogico.getModel();
        DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) arbol.getRoot();
        raiz.removeAllChildren();
        for (Familiares temp : familiares) {

            DefaultMutableTreeNode padre = new DefaultMutableTreeNode(temp.getFamiliar_padre().getNombre());
            DefaultMutableTreeNode hijos = new DefaultMutableTreeNode("Hijos");
            for (Familiares t : this.hijos) {
                    DefaultMutableTreeNode hijo = new DefaultMutableTreeNode(t.getNombre());
                    hijos.add(hijo);
                

            }
            padre.add(hijos);
            raiz.add(padre);
            arbol.reload();
        }
    }
